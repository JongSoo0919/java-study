package array;

public class Array2 {
    public static void main(String[] args) {
        int [] students = new int[5];

        students[0] = 30;
        students[1] = 40;
        students[2] = 50;
        students[3] = 60;
        students[4] = 70;

        for(int i = 0; i < 5; i++){
            System.out.println(students[i]);
        }
    }
}

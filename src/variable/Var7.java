package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 10; // 정수
        double d = 10.5; // 실수
        boolean b = true; // 참 or 거짓
        char c = 'A'; // 문자 하나
        String s = "str"; // 문자열 (특수 타입)

        System.out.println("a = " + a);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}
